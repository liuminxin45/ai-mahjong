export const CHENGDU_RULES = `
## 成都麻将（血战到底）规则

### 一、基本信息
- **规则名称**: 成都麻将 / 血战到底
- **玩家数量**: 4人
- **使用牌张**: 108张（万、条、筒各36张，无字牌、无花牌）
- **手牌数量**: 13张（摸牌后14张）
- **无吃牌**: 可碰可杠不可吃
- **无庄家**: P0固定先手

### 二、游戏流程

#### 1. 换三张阶段
- 游戏开始时，每位玩家选择3张**同花色**的牌与其他玩家交换
- 交换方向**固定顺时针**（P0→P1, P1→P2, P2→P3, P3→P0）
- 所有玩家同时确认后执行交换

#### 2. 定缺阶段
- 换三张后，每位玩家必须选择一种花色作为"缺门"
- 定缺后，**必须先打完缺门的牌**才能胡牌
- 胡牌时手牌中不能有定缺花色的牌
- **定缺花色不能碰也不能杠**

#### 3. 出牌阶段（血战到底）
- **血战到底**: 一家胡牌后游戏继续，直到3家胡牌或牌墙摸完
- 已胡牌的玩家退出游戏，不再参与出牌和点炮
- 未胡牌的玩家继续游戏
- **过水不能胡**: 可以胡但选择跳过后，本圈不能再胡别人弃牌，直到自己摸打后解除
- **一炮多响**: 同一弃牌可被多家胡牌，各自与放炮者1v1结算

### 三、胡牌条件

#### 基本胡牌型
- **4组面子 + 1对将**: 标准胡牌型
  - 面子: 顺子（如123万）或刻子（如333条）
  - 将: 一对相同的牌（如55筒）
- **七对子**: 7对（14张牌恰好7对）

#### 胡牌前提
1. 必须完成"缺一门"（定缺的花色牌已全部打出，手牌和副露中不含定缺花色）
2. 手牌形成有效的胡牌型
3. 不在过水限制中（自摸胡不受此限制）

### 四、番型与计分

#### 番型列表

| 番型 | 番数 | 说明 |
|------|------|------|
| **平胡** | 1番 | 基础胡牌，无特殊番型时兜底 |
| **自摸** | 1番 | 自己摸到胡牌（可与其他番叠加） |
| **对对胡** | 2番 | 4个刻子+1对将（无顺子） |
| **清一色** | 2番 | 全部同一花色 |
| **七对子** | 2番 | 7对结构 |
| **龙七对** | 3番 | 七对中含4张同牌（替代七对子，不叠加） |
| **全带幺** | 2番 | 每组面子和将都带1或9 |
| **金钩钓** | 2番 | 胡牌张出现在将牌组中 |
| **杠上开花** | 2番 | 杠后补牌自摸胡 |
| **抢杠胡** | 2番 | 别人加杠时抢胡 |
| **海底捞月** | 2番 | 牌墙最后阶段胡牌 |
| **刮风下雨** | 2番 | PENG同牌移除后可胡（特殊路径） |
| **天胡** | 4番 | P0在首回合自摸胡 |
| **地胡** | 4番 | 非P0对P0首张弃牌点炮胡 |

#### 番型互斥与叠加
- 龙七对替代七对子（不叠加）
- 平胡为兜底，有其他番时不出现
- 清一色 + 对对胡可叠加
- 自摸可与任何番叠加

#### 计分规则
- **计分公式**: 底分5 × 2^(总番数 - 1)
- **总番数** = 所有番型番数之和 + 杠数（每个GANG副露+1番）
- 示例: 平胡(1) + 自摸(1) = 2番 → 5 × 2^1 = 10分
- 示例: 清一色(2) + 对对胡(2) + 自摸(1) + 1杠 = 6番 → 5 × 2^5 = 160分

#### 结算方式
- **自摸（逐家收取）**: 每个未胡玩家各付score分（不是均摊），胡者得score×未胡人数
- **点炮**: 放炮者独自付score分
- **一炮多响**: 放炮者对每个胡牌者分别付全额

### 五、杠牌与雨钱

#### 碰
- 别人打出的牌与你手中2张相同 → 可以碰（定缺花色除外）
- 碰后形成明刻，需要打出1张牌
- 碰后不能立即打回碰的那张牌

#### 杠
- **暗杠**: 手中4张相同的牌（非定缺花色）
- **明杠**: 别人打出的牌与你手中3张相同（非定缺花色）
- **加杠**: 已碰的牌再摸到第4张（非定缺花色）
- 杠后补1张牌

#### 杠牌雨钱（即时结算）
- **暗杠**: 向所有未胡玩家各收10分
- **明杠**: 向点杠者收10分
- **加杠**: 向原碰牌来源者收5分

#### 抢杠胡
- 当其他玩家加杠时，如果该牌能让你胡牌，可以抢杠胡
- 视为放炮，由加杠者付分
- 抢杠胡不受过水限制

### 六、流局结算

牌墙摸完时（非3人胡牌结束），对未胡玩家进行额外惩罚：

#### 查花猪
- 花猪：未胡且手牌/副露中仍有定缺花色的玩家
- 花猪向每个非花猪未胡玩家赔付对方最大听牌得分

#### 查叫
- 在非花猪的未胡玩家中：未听牌者向听牌者赔付其最大可能胡牌得分

### 七、策略建议（参考，非绝对）

#### 定缺策略
- 通常选择手牌中数量较少的花色作为缺门
- 但如果追求清一色等高番型，可能选择定掉张数少但会破坏主攻花色的那门
- 考虑整体牌型发展方向

#### 进攻策略
- 一般优先清掉缺门牌
- 保持手牌顺畅度（相邻的牌容易组成顺子）
- 追求高番型（清一色、对对胡）时策略会有所不同

#### 防守策略
- 观察其他玩家的弃牌，判断其定缺和听牌可能
- 打熟张（别人打过的牌）相对安全
- 后期注意防止点炮，但也要权衡自己的胡牌机会
- 注意过水规则：跳过胡牌后本圈不能再胡弃牌

#### 血战到底特点
- 一家胡牌后放炮惩罚相对更重
- 牌局后期危险牌增多，需要权衡进攻与防守
- 已胡玩家不会再点炮和参与雨钱，可以适当调整打法
- 后胡的玩家可能获得更高收益（前面胡的人不再参与）
- 流局时花猪和未听牌者会被罚分
`;

