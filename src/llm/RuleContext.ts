export const CHENGDU_RULES = `
## 成都麻将（血战到底）规则

### 一、基本信息
- **规则名称**: 成都麻将 / 血战到底
- **玩家数量**: 4人
- **使用牌张**: 108张（万、条、筒各36张，无字牌、无花牌）
- **手牌数量**: 13张（摸牌后14张）

### 二、游戏流程

#### 1. 换三张阶段
- 游戏开始时，每位玩家选择3张**同花色**的牌与其他玩家交换
- 交换方向随机（顺时针或逆时针）
- 所有玩家同时确认后执行交换

#### 2. 定缺阶段
- 换三张后，每位玩家必须选择一种花色作为"缺门"
- 定缺后，**必须先打完缺门的牌**才能胡牌
- 胡牌时手牌中不能有定缺花色的牌

#### 3. 出牌阶段（血战到底）
- **血战到底**: 一家胡牌后游戏继续，直到3家胡牌或牌墙摸完
- 已胡牌的玩家退出游戏，不再参与出牌和点炮
- 未胡牌的玩家继续游戏

### 三、胡牌条件

#### 基本胡牌型
- **4组面子 + 1对将**: 标准胡牌型
  - 面子: 顺子（如123万）或刻子（如333条）
  - 将: 一对相同的牌（如55筒）
- **七对子**: 7对不同的对子

#### 胡牌前提
1. 必须完成"缺一门"（定缺的花色牌已全部打出）
2. 手牌形成有效的胡牌型

### 四、番型与计分

#### 番型列表（番数越高，得分越高）

| 番型 | 番数 | 说明 |
|------|------|------|
| **平胡** | 1番 | 基础胡牌，无特殊番型 |
| **自摸** | 1番 | 自己摸到胡牌 |
| **门清** | 1番 | 没有碰/杠（无副露） |
| **对对胡** | 2番 | 4个刻子+1对将（无顺子） |
| **全带幺** | 2番 | 每组面子都带1或9 |
| **金钩钓** | 2番 | 单钓将胡牌 |
| **杠上开花** | 2番 | 杠后摸的牌刚好胡牌 |
| **抢杠胡** | 2番 | 别人加杠时抢胡 |
| **海底捞月** | 2番 | 摸最后一张牌胡牌 |
| **清一色** | 3番 | 全部同一花色 |
| **七对子** | 3番 | 7对不同的对子 |

#### 计分规则
- 基础分根据番数计算：
  - 1番: 500分
  - 2番: 1000分
  - 3番: 1500分
  - 4-5番: 2000分
  - 6-7番: 3000分
  - 8-10番: 4000分
  - 11-12番: 6000分
  - 13番以上: 8000分（封顶）

- **根**: 每有1个杠，分数翻倍
- **自摸**: 其他未胡玩家均摊付分
- **点炮**: 放炮者独自付分

### 五、特殊动作

#### 碰
- 别人打出的牌与你手中2张相同 → 可以碰
- 碰后形成明刻，需要打出1张牌

#### 杠
- **明杠**: 别人打出的牌与你手中3张相同
- **暗杠**: 手中4张相同的牌
- **加杠**: 已碰的牌再摸到第4张
- 杠后从牌墙末尾补1张牌

#### 抢杠胡
- 当其他玩家加杠时，如果该牌能让你胡牌，可以抢杠胡
- 视为放炮，由加杠者付分

### 六、策略建议（参考，非绝对）

#### 定缺策略
- 通常选择手牌中数量较少的花色作为缺门
- 但如果追求清一色等高番型，可能选择定掉张数少但会破坏主攻花色的那门
- 考虑整体牌型发展方向

#### 进攻策略
- 一般优先清掉缺门牌
- 保持手牌顺畅度（相邻的牌容易组成顺子）
- 门清加番，但碰杠可以加快听牌，需权衡
- 追求高番型（清一色、对对胡）时策略会有所不同

#### 防守策略
- 观察其他玩家的弃牌，判断其定缺和听牌可能
- 打熟张（别人打过的牌）相对安全
- 后期注意防止点炮，但也要权衡自己的胡牌机会

#### 血战到底特点
- 一家胡牌后放炮惩罚相对更重
- 牌局后期危险牌增多，需要权衡进攻与防守
- 已胡玩家不会再点炮，可以适当调整打法
- 后胡的玩家可能获得更高收益（前面胡的人不再参与）
`;

